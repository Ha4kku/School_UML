public class Person {

    private String name;
    private int id;

    public Person(String n, int id) {
        this.name = n;
        this.id = id;

    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }



    // getters
}